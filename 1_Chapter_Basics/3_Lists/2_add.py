### Создаем список с данными:
motorcycles = ['honda', 'yamaha', 'suzuki']
print(motorcycles)

# ['honda', 'yamaha', 'suzuki']

### Добовляем новый элемент в массив 'ducati' вместо 'honda':
motorcycles[0] = 'ducati'
print(motorcycles)

# ['ducati', 'yamaha', 'suzuki']

### Пример наполнения списка:
motorcycles_new = []
motorcycles_new.append('moto_0')
motorcycles_new.append('moto_1')
motorcycles_new.append('moto_2')
print(motorcycles_new)

# ['moto_0', 'moto_1', 'moto_2']
