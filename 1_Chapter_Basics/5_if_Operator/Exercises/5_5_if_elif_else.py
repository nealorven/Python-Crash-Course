### if-elif-else:
alien_color = [
    'green',
    'yellow',
    'red'
    ]
alien_healt = alien_color[2]

if 'green' in alien_healt: print("You got 5 points.")
elif 'yellow' in alien_healt: print("You got 10 points.")
elif 'red' in alien_healt: print("You got 15 points.")
else: print("You have no points.")

# You got 15 points.
